    public void addShowResult() {
        ttNhanVien nv = list.get(list.size() - 1);
        model.addRow(new Object[]{
            nv.getMaNV(), nv.getHoTen(), nv.getSDT(), nv.getGioiTinh(), nv.getNgaySinh(), nv.getChucVu(), nv.getDiaChi()
        });
    }
    
    public void addLuongShowResult() {
        ttNhanVien nv = list2.get(list2.size() - 1);
        model2.addRow(new Object[]{
            nv.getMaNV(), nv.getHoTen(), nv.getThangLam(), nv.getLuong()
        });
    }
    
    public void delShowResult() {
        ttNhanVien nv = list.get(list.size() - 1);
        model.removeRow(tblNhanVien.getSelectedRow());
    }
    
    
