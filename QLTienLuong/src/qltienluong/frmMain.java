    private void btnTinhLuongActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTinhLuongActionPerformed
        // TODO add your handling code here:
        String month = cbxThang2.getSelectedItem().toString();
        String year = cbxNam2.getSelectedItem().toString();
        String mnv = txtMaNVTinh.getText();
        float tongGio = 0.0f;
        float tongLuong = 0.0f;
        float luong1h = 24000;
        String cv = "";
        
        if (mnv.equals("")){
            JOptionPane.showMessageDialog(this, "Vui lòng nhập Mã NV cần tính lương!");
            txtMaNVTinh.requestFocus();
        } else {
            for (ttNhanVien nv : list2){
                if (nv.getNgayLam().substring(3, 5).equals(month) && nv.getMaNV().equals(mnv)){
                    float gio = Float.parseFloat(nv.getGioLam());
                    tongGio = tongGio + gio;
                    cv = nv.getChucVu();
                }
            }
            if (cv.substring(0, 9).equals("Nhân Viên")){
                tongLuong = tongGio * luong1h;
                String tL = String.valueOf(tongLuong);
                txtTongLuong.setText(tL);
            } else {
                tongLuong = tongGio * luong1h * 1.25f;
                String tL = String.valueOf(tongLuong);
                txtTongLuong.setText(tL);
            }
            
        }
        
           
    }//GEN-LAST:event_btnTinhLuongActionPerformed

    private void btnThemBLActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemBLActionPerformed
        // TODO add your handling code here:
        ttNhanVien nv = new ttNhanVien();
        String mnvNow = txtMaNVTinh.getText();
        String ht = "";
        String thg = "";
        
        for (ttNhanVien nvNow : list2){
            if (nvNow.getMaNV().equals(mnvNow)){
                ht = nvNow.getHoTen();
                thg = nvNow.getNgayLam().substring(3, 10);
            }
        }
        
        nv.setMaNV(mnvNow);
        nv.setHoTen(ht);
        nv.setThangLam(thg);
        nv.setLuong(txtTongLuong.getText());
       
        if(new DAOLuong().themVaoBLuong(nv)){
            JOptionPane.showMessageDialog(rootPane, "Thêm thành công!");
            list2.add(nv);
            addLuongShowResult();
        } else{
            JOptionPane.showMessageDialog(rootPane, "Thêm không thành công!");
        }
    }//GEN-LAST:event_btnThemBLActionPerformed
    
