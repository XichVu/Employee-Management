    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        String err = "";
        
        String diachi = txtDiaChi.getText();
        if (diachi.equals("")){
            err = "Vui lòng nhập Địa chỉ!\n\n" +err;
            txtDiaChi.requestFocus();
        }
        

        String sdt = txtSDT.getText();
        if (sdt.equals("")){
            err = "Vui lòng nhập Số điện thoại!\n\n" +err;
            txtSDT.requestFocus();
        }
        else {
            for (int i=0; i<sdt.length(); i++)
            if (sdt.charAt(i) > '9' || sdt.charAt(i) < '0'){
                err = "Vui lòng nhập Số điện thoại là chữ số!\n\n" +err;
                txtSDT.requestFocus();
                break;
            }
        }
        
        String hoten = txtHoTen.getText();
        if (hoten.equals("")){
            err = "Vui lòng nhập Họ tên!\n\n" +err;
            txtHoTen.requestFocus();
        }
        
        String manv = txtMaNV.getText();
        if (manv.length() != 5){
                err = "Vui lòng nhập Mã NV với định dạng 5 chữ số!\n\n" +err;
                txtMaNV.requestFocus();
        }
        else {
            for (int i=0; i<manv.length(); i++)
            if (manv.charAt(i) > '9' || manv.charAt(i) < '0'){
                err = "Vui lòng nhập Mã NV là chữ số!\n\n" +err;
                txtMaNV.requestFocus();
                break; 
            }
        }

        ButtonModel gioitinh = btnGroupGioiTinh.getSelection();
        String gT = "";
        if (radNam.isSelected()) gT = "Nam";
        if (radNu.isSelected()) gT = "Nữ";

        String nams = cbxNam.getSelectedItem().toString();
        int year = Integer.parseInt(nams);
        String thangs = cbxThang.getSelectedItem().toString();
        int month = Integer.parseInt(thangs);
        String ngays = cbxNgay.getSelectedItem().toString();
        int day = Integer.parseInt(ngays);

        if (checkYear(year) == true){
            if (month == 2){
                if (day > 29){
                    err = "Vui lòng nhập ngày sinh đúng!\n\n" +err;
                    cbxNgay.requestFocus();
                }
            } else {
                if (month == 4 || month == 6 || month == 9 || month == 11){
                    if (day > 30){
                        err = "Vui lòng nhập ngày sinh đúng!\n\n" +err;
                        cbxNgay.requestFocus();
                    }
                }
            }
        } else {
            if (month == 2){
                if (day > 28){
                    err = "Vui lòng nhập ngày sinh đúng!\n\n" +err;
                    cbxNgay.requestFocus();
                }
            } else {
                if (month == 4 || month == 6 || month == 9 || month == 11){
                    if (day > 30){
                        err = "Vui lòng nhập ngày sinh đúng!\n\n" +err;
                        cbxNgay.requestFocus();
                    }
                }
            }
        }

        String chucvu = cbxChucVu.getSelectedItem().toString();

        if (err.length() > 0) JOptionPane.showMessageDialog(this, err);
        
        else {
            String dob = ngays+ "/" +thangs+ "/" +nams;

            ttNhanVien nv = new ttNhanVien();
            nv.setMaNV(txtMaNV.getText());
            nv.setHoTen(txtHoTen.getText());
            nv.setSDT(txtSDT.getText());
            nv.setGioiTinh(gT);
            nv.setNgaySinh(dob);
            nv.setChucVu(cbxChucVu.getSelectedItem().toString());
            nv.setDiaChi(txtDiaChi.getText());

            if(new DAO().themNhanVien(nv)){
                JOptionPane.showMessageDialog(rootPane, "Thêm thành công!");
                list.add(nv);
                addShowResult();
            } else{
                JOptionPane.showMessageDialog(rootPane, "Thêm không thành công, kiểm tra lại các thông tin đã nhập!");
            }
        }

    }//GEN-LAST:event_btnThemActionPerformed

