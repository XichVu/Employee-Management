    private void tblNhanVienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblNhanVienMouseClicked
        // TODO add your handling code here:
        int index = tblNhanVien.getSelectedRow();
        TableModel model = tblNhanVien.getModel();
        
        String manv = model.getValueAt(index, 0).toString();
        String hoten = model.getValueAt(index, 1).toString();
        String sdt = model.getValueAt(index, 2).toString();
        
        String gioitinh = model.getValueAt(index, 3).toString();
        String gt = gioitinh.substring(0, 3);
        
        String ngaysinh = model.getValueAt(index, 4).toString();
        String ngay = ngaysinh.substring(0, 2);
        int day = Integer.parseInt(ngay);
        String thang = ngaysinh.substring(3, 5);
        int month = Integer.parseInt(thang);
        String nam = ngaysinh.substring(6, 10);
        int year = Integer.parseInt(nam);
        
        String chucvu = model.getValueAt(index, 5).toString();
        String cv = chucvu.substring(0, 11);
        
        String diachi = model.getValueAt(index, 6).toString();

        txtMaNV.setText(manv);
        txtHoTen.setText(hoten);
        txtSDT.setText(sdt);
        
        if (gt.equalsIgnoreCase("Nam")) radNam.setSelected(true);
        else radNu.setSelected(true);
        
        cbxNgay.setSelectedIndex(day - 1);
        cbxThang.setSelectedIndex(month - 1);
        cbxNam.setSelectedIndex(year - 1960);
        
        if (cv.equalsIgnoreCase("Trưởng Nhóm")) cbxChucVu.setSelectedIndex(1);
        else cbxChucVu.setSelectedIndex(0);
        
        txtDiaChi.setText(diachi);
    }//GEN-LAST:event_tblNhanVienMouseClicked

